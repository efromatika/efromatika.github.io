function handleAngkatanChange() {
    const angkatan = document.getElementById("angkatanSelect").value;
    const katingSelect = document.getElementById("katingSelect");
    katingSelect.innerHTML = '<option value="">-- Pilih Kating --</option>';
    if (!angkatan) return;
    const list = katingData[angkatan] || [];
    list.forEach(nama => {
        if (!selectedKatings.includes(nama)) {
            const opt = document.createElement("option");
            opt.value = nama;
            opt.textContent = nama;
            katingSelect.appendChild(opt);
        }
    });
}

async function addKating() {
    const katingSelect = document.getElementById("katingSelect");
    const val = katingSelect.value;
    if (!val) return alert("Pilih kating!");
    if (selectedKatings.length >= 4) return alert("Maksimal 4 kating!");
    
    // Cek syarat kating
    if (katingRequirements[val] !== undefined) {
        const requiredSharings = katingRequirements[val];
        
        // Hitung jumlah completed sharing yang melibatkan user
        const completedSnap = await db.collection("completedSchedules")
            .where("participants", "array-contains", currentUser.email)
            .get();
        const currentSharingCount = completedSnap.size;
        
        if (currentSharingCount < requiredSharings) {
            return alert(`Kating "${val}" belum memenuhi syarat. Diperlukan minimal ${requiredSharings} sharing (saat ini: ${currentSharingCount}).`);
        }
    }
    
    selectedKatings.push(val);
    renderSelected();
    handleAngkatanChange();
}

function removeKating(index) {
    selectedKatings.splice(index, 1);
    renderSelected();
    handleAngkatanChange();
}

function updateTaskCounters() {
    const pendingTasks = allTasks.filter(task => {
        return !myTaskSubmissions.find(submission => submission.taskId === task.id);
    });
    const completedTasksCount = myTaskSubmissions.length;
    
    const countPendingTasksEl = document.getElementById("countTasks");
    const countCompletedTasksEl = document.getElementById("countCompletedTasks");
    if (countPendingTasksEl) {
        countPendingTasksEl.innerText = pendingTasks.length;
    }
    if (countCompletedTasksEl) {
        countCompletedTasksEl.innerText = completedTasksCount;
    }
}

function listenTasks() {
    db.collection("taskSubmissions").where("userEmail", "==", currentUser.email)
        .onSnapshot(snap => {
            myTaskSubmissions = snap.docs.map(doc => doc.data());
            renderTasksList();
            updateTaskCounters();
        });
    db.collection("tasks").orderBy("createdAt", "desc")
        .onSnapshot(snap => {
            allTasks = snap.docs.map(doc => ({
                id: doc.id,
                ...doc.data()
            }));
            renderTasksList();
            updateTaskCounters();
        });
}

function renderTasksList() {
    const pendingList = document.getElementById("pendingTasksList");
    const completedList = document.getElementById("completedTasksList");
    if (!pendingList || !completedList) return;

    pendingList.innerHTML = "";
    completedList.innerHTML = "";

    allTasks.forEach(task => {
        const submission = myTaskSubmissions.find(s => s.taskId === task.id);
        const isCompleted = !!submission;
        const deadlineDate = new Date(task.deadline);
        const now = new Date();
        const isPastDeadline = now > deadlineDate;
        const card = document.createElement("div");
        card.className = "bg-white p-5 rounded-xl shadow border-l-4 " + (isCompleted ? "border-blue-500" : (isPastDeadline ? "border-red-500" : "border-yellow-500"));
        card.innerHTML = `
            <div class="flex justify-between items-start mb-2">
                <h3 class="font-bold text-gray-800">${task.title}</h3>
                ${isCompleted ? '<span class="bg-green-100 text-green-700 text-[10px] px-2 py-1 rounded-full">Selesai</span>' : ''}
            </div>
            <p class="text-xs text-gray-600 mb-3">${task.desc}</p>
            <div class="text-[10px] text-gray-500 mb-4 space-y-1">
                <p>Format File: ${task.fileType}</p>
                <p class="${!isCompleted && isPastDeadline ? 'text-red-500 font-bold' : ''}">🕐 Deadline: ${deadlineDate.toLocaleString('id-ID', {timeZone: 'Asia/Jakarta'})} WIB</p>
                ${isCompleted ? `<p class="text-blue-500">Dikumpulkan: ${new Date(submission.submittedAt.seconds * 1000).toLocaleString('id-ID', {timeZone: 'Asia/Jakarta'})} (${submission.status})</p>` : ''}
            </div>
            <div class="flex justify-between items-center gap-2">
                ${!isCompleted ? `<button onclick="openUploadTaskModal('${task.id}', '${task.fileType}', '${task.deadline}', '${task.idFormat}')" class="flex-1 bg-blue-500 text-white text-xs py-2 rounded hover:bg-blue-600">Upload Tugas</button>` : ''}
                ${isAdmin() ? `<button onclick="viewSubmissions('${task.id}')" class="bg-gray-100 text-gray-700 text-xs py-2 px-3 rounded hover:bg-gray-200">Lihat Pengumpulan</button>` : ''}
                ${isAdmin() ? `<button onclick="deleteTask('${task.id}')" class="bg-red-50 text-red-500 text-xs py-2 px-3 rounded hover:bg-red-100">Hapus</button>` : ''}
            </div>
        `;

        if (isCompleted) completedList.appendChild(card);
        else pendingList.appendChild(card);
    });

    if (pendingList.innerHTML === "") pendingList.innerHTML = "<p class='text-sm text-gray-400 italic'>Tidak ada tugas yang tertunda.</p>";
    if (completedList.innerHTML === "") completedList.innerHTML = "<p class='text-sm text-gray-400 italic'>Belum ada tugas yang diselesaikan.</p>";
}

